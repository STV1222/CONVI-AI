export function getProjectById(projectId: string) {
  return projects.find((project) => project.id === projectId);
}

export function getProjectName(projectId: string) {
  return getProjectById(projectId)?.name ?? "Portfolio";
}

export function getProjectProviders(projectId: string) {
  return providers.filter((provider) => provider.activeProjects.includes(projectId));
}

export function getProjectWorkPackages(projectId: string) {
  return workPackages.filter((workPackage) => workPackage.projectId === projectId);
}

export function getProjectProcurement(projectId: string) {
  return procurementItems.filter((item) => item.projectId === projectId);
}

export function getProjectSafety(projectId: string) {
  return safetyRecords.filter((item) => item.projectId === projectId);
}

export function getProjectEquipment(projectId: string) {
  return equipmentAssets.filter((item) => item.projectId === projectId);
}

export function getProjectProgress(projectId: string) {
  return progressUpdates.filter((item) => item.projectId === projectId);
}

export function getProjectAlerts(projectId: string) {
  return aiAlerts.filter((alert) => alert.projectId === projectId);
}

